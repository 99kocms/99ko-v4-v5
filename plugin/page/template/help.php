<h3>Ne pas afficher dans le menu</h3>
<p>Permet de ne pas afficher d'item dans le menu, pour créer une page profonde ou ne pas la publier tout de suite, par exemple.</p>
<h3>Item parent</h3>
<p>Permet de placer l'item dans un tant que sous menu de son parent.</p>
<h3>Classe CSS</h3>
<p>Ajoute la classe CSS spécifiée à l'item du menu.</p>
<h3>Position</h3>
<p>Position de l'item dans le menu.</p>
<h3>Restreindre l'accès avec un mot de passe</h3>
<p>Le contenu ne pourra être affiché sans le mot de passe saisi.</p>
<h3>Titre de page</h3>
<p>Permet d'afficher un titre de page différent de l'item du menu.</p>
<h3>Inclure un fichier .php au lieu du contenu</h3>
<p>Permet de remplaçer le contenu par un fichier dynamique. Celui-ci doit être présent dans le dossier de votre thème.</p>
<h3>Image à la une</h3>
<p>Permet de mettre en avant une image (le plugin Galerie doit être installé et activé).</p>