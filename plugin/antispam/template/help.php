<h3>Text Captcha</h3>
<p>Il s'agit d'un simple Captcha où l'utilisateur doit effectuer une simple opération
mathématique.</p>
<h3>Google ReCaptcha</h3>
<p>Captcha plus performant, mais nécessite une <a href="https://www.google.com/recaptcha/admin/create" target="_blank">inscription</a> pour pouvoir récupérer les 2 clés d'API.</p>